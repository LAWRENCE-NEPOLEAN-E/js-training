console.log("Testing Unary operators:");

let number1 = 5;
let number2 = 10

console.log(number1++); //post-increment
console.log(number1--); //post-decreament
console.log(++number2); //pre-increment
console.log(--number2); //pre-decrement
console.log(+"1");  //unary plus    
console.log(-"1");  //unary minus